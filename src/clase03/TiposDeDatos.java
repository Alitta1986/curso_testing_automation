package clase03;

public class TiposDeDatos {
    public static void main(String[] args) {
        int count;
        count = 100;
        System.out.println(count);
        float f;
        f = 1.1234f;
        System.out.println(f);
        boolean bool = false;
        System.out.println(bool);
        char ch = '!';
        System.out.println(ch);
    }


}
